def letter_printer(lettr, format):
    for line in lettr:
        length = len(line)
        count = 0
        for charac in line:
            if charac == 1 and count == length - 1:
                if format == "v":
                    print("x")
                if format == "h":
                    print("x", end="  ")
            elif charac == 1:
                print("x", end="")
            elif charac == 0 and count == length - 1:
                if format == "v":
                    print(" ")
                if format == "h":
                    print(" ", end="  ")
            else:
                print(" ", end="")
            count += 1


